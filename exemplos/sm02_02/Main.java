// É uma boa prática nomear a classe com o mesmo nome do arquivo (Exemplo2.java)
public class Exemplo2 {

    // O método 'main' é onde a execução do programa começa
    public static void main(String[] args) {


        // 1. Declaração das variáveis
        int x;      // Variável para um número inteiro.
        double y;   // Variável para um número com casas decimais.

        // 2. Atribuição de valores
        x = 5;          // A variável 'x' agora vale 5.
        y = 2 * x;      // 'y' recebe o resultado de 2 * 5, que é 10.0.

        // 3. Impressão dos resultados no console
        System.out.println(x); // Imprime o valor de x
        System.out.println(y); // Imprime o valor de y


    }
}